/*!
* \class calcul
* \brief
* Permet de faire des calculs
*/
class calcul{

public:
	/*! \brief description somme
	* \param nb1 premier nb
	* \param nb2 deuxieme nb
	* \return la somme des 2 nb
	*/
	int somme(int nb1, int nb2);
	/*! \brief description multiplication
	* \param nb1 premier nb
	* \param nb2 deuxieme nb
	* \return la multiplication des 2 nb
	*/
	int multiplication(int nb1, int nb2);
	/*! \brief description division
	* \param nb1 premier nb
	* \param nb2 deuxieme nb
	* \return la division des 2 nb
	*/
	int division(int nb1, int nb2);
	/*! \brief description soustraction
	* \param nb1 premier nb
	* \param nb2 deuxieme nb
	* \return la soustraction des 2 nb
	*/
	int soustraction(int nb1, int nb2);
};
